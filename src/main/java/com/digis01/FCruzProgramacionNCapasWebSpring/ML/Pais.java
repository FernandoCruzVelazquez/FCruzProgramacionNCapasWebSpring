public class Pais {
    
    private Integer IdPais;
    private String Nombre;
    
    public Integer getIdPais(){
        return IdPais;
    }
    
    public void setIdPais(Integer IdPais){
        this.IdPais = IdPais;
    }
    
    public String getNombre(){
        return Nombre;
    }
    
    public void setNombre(String Nombre){
        this.Nombre = Nombre;
    }
    
}
